number = input().split()

a, b = int(number[0]), int(number[1])

if a >= b:
    print(1)
else:
    print(0)

if a > b:
    print(1)
else:
    print(0)

if b >= a:
    print(1)
else:
    print(0)

if b > a:
    print(1)
else:
    print(0)

if a == b:
    print(1)
else:
    print(0)

if a != b:
    print(1)
else:
    print(0)